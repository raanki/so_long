int	ft_atoi(const char *s)
{
	int		sign;
	int		n;
	int		i;

	n = 0;
	sign = 1;
	i = 0;
	while (s[i] == '\f' || s[i] == ' ' || s[i] == '\n'
		|| s[i] == '\t' || s[i] == '\v' || s[i] == '\r' )
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	while (s[i] != 0 && s[i] >= '0' && s[i] <= '9')
	{
		n = 10 * n + (s[i] - '0');
		i++;
	}
	return (n * sign);
}
